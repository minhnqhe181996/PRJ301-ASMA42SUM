    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        Users user = (Users) session.getAttribute("user");
        if (user == null) {
            response.sendRedirect("Login");
        } else {
            response.setContentType("text/html;charset=UTF-8");
            ArrayList<Users> account = UserDAO.getUsers();
            ArrayList<Registrations> list = RegistrationsDAO.getRegistrations();
            String action = request.getParameter("action");

            if (action == null) {
                RequestDispatcher rs = request.getRequestDispatcher("view/trang_police.jsp");
                rs.forward(request, response);
            }
            else if(action.equals("kiem_duyet")){
                String id=request.getParameter("id");
                Registrations kd=RegistrationsDAO.getRegById(Integer.parseInt(id));
                request.setAttribute("list", kd);
                RequestDispatcher rs = request.getRequestDispatcher("view/trang_duyet_thong_tin.jsp");
                rs.forward(request, response);
            }
        }
    }

